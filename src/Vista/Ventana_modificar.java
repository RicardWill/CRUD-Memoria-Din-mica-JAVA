    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Alumno valor = control.BuscaAlumno(txtMatricula.getText());
        
        if(valor == null){
            txtNombre.setText("");
            txtCarrera.setText("");
            txtEdad.setText("");
            JOptionPane.showMessageDialog(null, "El valor buscado no existe");
            
        }else{
            
            txtNombre.setText(valor.getNombre());
            txtCarrera.setText(valor.getCarrera());
            txtEdad.setText(String.valueOf(valor.getEdad()));
            
            //lblEdad.setText(valor.());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

